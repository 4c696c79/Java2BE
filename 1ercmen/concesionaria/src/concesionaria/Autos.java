package concesionaria;

public class Autos {
    private String marca;
    private String modo;
    private String matri;

    public String getMaca() {
        return marca;
    }

    public void setMaca(String maca) {
        this.marca = maca;
    }

    public String getModo() {
        return modo;
    }

    public void setModo(String modo) {
        this.modo = modo;
    }

    public String getMa() {
        return matri;
    }

    public void setMa(String ma) {
        this.matri = ma;
    }
    
}
