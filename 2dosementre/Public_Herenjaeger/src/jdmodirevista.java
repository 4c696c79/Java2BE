    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       /*   En el boton modificar pones los datos primero hice un if 
        para ver si el cuadro de texto de modificar esta vacio si lo
        esta mostrara un mensaje.
            Si no esta vacio y pones la revista a modificar y los datos
        se hace un for para poder acceder al arraylist y un if para ver si
        la revista existente coincide con el cuadro de texto "cmodirevi".
            En ese caso lo q hace es q se guarda la posicion del array
        con el int 'posision' y se accede a los datos de la revista y se 
        reemplaza con los nuevos datos.
        jframe para acceder el arraylist revistas, .revistas.get(i) para 
        recibir el libro y sus datos, .setTitulo(cname3.getText()) para 
        introducir los nuevos datos q seran reemplazados.*/
        for (int i = 0; i < jframe.revistas.size(); i++) {
            if (cmodirevi.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "No hay nada, pon el titulo para modificar");
            } else if (jframe.revistas.get(i).getTitulo().equals(cmodirevi.getText())) {
                posision++;
                jframe.revistas.get(i).setTitulo(cname3.getText());
                jframe.revistas.get(i).setA침o(cano3.getText());
                jframe.revistas.get(i).setTipo(ctipo2.getText());
                if (jCheckBox1.isSelected()) {
                    jframe.revistas.get(i).setPretado(true);
                } else {
                    jframe.revistas.get(i).setPretado(false);
                }
                //luego aparecerea el mensaje q se modifico con exito
                JOptionPane.showMessageDialog(null, "Revista modificada 픧(._. )>");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        /*   Aca es para mostrar los datos de las revistas.
            Primero hace un if para ver si tiene datos el arraylist revistas,
        si no tiene datos te aparece el mensaje "No hay revista"
            En caso que si hay datos, mostrara los datos.*/
        
        /*  Aca como los datos esta en el jframe y no aqui, primero se
        pone jframe y luego un punto, para luego poner el nombre de tu
        arraylist (revista en este caso) y luego es igual libros.get(i)
        para recibir los datos segun la posicion del array en el for y
        .getTitulo() para mostrar en este caso los datos del libro*/
        
        if (jframe.revistas.isEmpty()) {
            JOptionPane.showMessageDialog(null, "no hay revistas");

        } else {
            for (int i = 0; i < jframe.revistas.size(); i++) {
                JOptionPane.showMessageDialog(null, "Datos de la revista:\n"
                        + "Nombre del libro:\n" + jframe.revistas.get(i).getTitulo()
                        + "\nA침o de publicacion:\n" + jframe.revistas.get(i).getA침o()
                        + "\nTema del Libro:\n" + jframe.revistas.get(i).getTipo()
                        + "\nEs prestado?\n" + jframe.revistas.get(i).isPretado());

            }
        }

    }//GEN-LAST:event_jButton4ActionPerformed

