
package fabrica;
public class Productos {
    //pones los datos
private String nome;
private int precio;
private int codigo;
////////////////////////////////////////////////////////////////////////////////////
    //getters and setters
    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getPrecio() {
        return precio;
    }

    public void setPrecio(int precio) {
        this.precio = precio;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }







}
